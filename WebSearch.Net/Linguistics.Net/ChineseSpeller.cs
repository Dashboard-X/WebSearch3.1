        public static string Spell(string text)
        {
            byte[] bytes = Encoding.Default.GetBytes(text);
            StringBuilder ret = new StringBuilder();
            for (int i = 0, p; i < bytes.Length; i++)
            {
                p = (int)bytes[i];
                if (p > 160)
                {
                    p = p * 256 + bytes[++i] - 65536;
                    ret.Append(Convert(p));
                }
                else
                    ret.Append((char)p);
            }
            return ret.ToString();
        }

        private static string Convert(int num)
        {
            if (num < -20319 || num > -10247)
                return "";
            while (!SpellTable.ContainsKey(num))
                num--;
            return SpellTable[num].ToString();
        }

